"use client";

import { useEffect, useState } from "react";

export type Locale = "en" | "es" | "fr" | "de" | "pt" | "zh";

const supportedLocales: Locale[] = ["en", "es", "fr", "de", "pt", "zh"];

export function detectLocale(language?: string): Locale {
  const source = (language ?? "en").toLowerCase();
  const exact = supportedLocales.find((locale) => source === locale || source.startsWith(`${locale}-`));

  if (exact) {
    return exact;
  }

  if (source.startsWith("zh")) {
    return "zh";
  }

  return "en";
}

export function useLocale() {
  const [locale, setLocale] = useState<Locale>("en");

  useEffect(() => {
    queueMicrotask(() => setLocale(detectLocale(navigator.language)));
  }, []);

  return locale;
}

